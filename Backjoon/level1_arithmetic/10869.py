stringInput = input("")
numberList = stringInput.split(" ")

# A + B
print(int(numberList[0]) + int(numberList[1]))
# A - B
print(int(numberList[0]) - int(numberList[1]))
# A * B
print(int(numberList[0]) * int(numberList[1]))
# A / B
print(int(numberList[0]) // int(numberList[1]))
# A % B
print(int(numberList[0]) % int(numberList[1]))